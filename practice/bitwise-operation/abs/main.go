package main

import (
	"fmt"
)

func abs(num int) int {
	sign := num >> 63
	return (num ^ sign) - sign
}

func main() {
	for _, n := range []int{0, 3, -4, -10, 11} {
		fmt.Printf("%4d (%5b, %4b) -> %d(%5b)\n",
			n, n, uint(n),
			abs(n), abs(n))
	}
}
